#!/usr/bin/env node
// Bundles the upstream DeepFilterNet wasm-bindgen JS glue + our processor
// into one self-contained AudioWorklet file with zero ESM imports. Output:
// public/vendor/dfn3/worklet.js (committed). Run via `npm run bundle:dfn3`
// whenever the vendor df.js or processor source changes.
//
// Vendor WASM lives at public/vendor/dfn3/df_bg.wasm; vendor JS glue at
// public/vendor/dfn3/df.js (built with `wasm-pack build --target no-modules
// --features wasm` from Rikorose/DeepFilterNet libDF). Output `worklet.js`
// is self-contained — wasm/model bytes arrive at runtime via MessagePort.
//
// Why bundle: AudioWorkletGlobalScope rejects `import` and `import.meta`,
// so the glue must be inlined. The no-modules target uses an IIFE that
// assigns to global `wasm_bindgen` — fits the worklet pattern.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');

const dfJsPath = resolve(root, 'public/vendor/dfn3/df.js');
const processorPath = resolve(root, 'src/audio/dfn3.worklet.src.js');
const outPath = resolve(root, 'public/vendor/dfn3/worklet.js');

const dfJs = readFileSync(dfJsPath, 'utf8');
const processor = readFileSync(processorPath, 'utf8');

// df.js probes globals (`window`, `module`, `global`) inside wbg import
// callbacks via `handleError`, which catches the resulting ReferenceError.
// Stubbing them as undefined keeps reads safe (typeof works) without
// changing behavior — wasm-bindgen falls through to `globalThis`/`self`.
//
// TextEncoder/TextDecoder are absent from AudioWorkletGlobalScope on
// older Chromium (added in Chrome 105+, but not in all WebView builds).
// df.js needs TextDecoder to format Rust panic messages and any string
// returned from wasm. Polyfill UTF-8 only — that's all wasm-bindgen emits.
const preamble = `// AUTO-GENERATED by scripts/bundle-dfn3.mjs — do not edit.
// Source: public/vendor/dfn3/df.js + src/audio/dfn3.worklet.src.js
var window = undefined;
var global = undefined;
var module = undefined;

if (typeof TextDecoder === 'undefined') {
  globalThis.TextDecoder = class {
    constructor() {}
    decode(input) {
      if (!input) return '';
      const b = input instanceof Uint8Array ? input
        : input.buffer ? new Uint8Array(input.buffer, input.byteOffset, input.byteLength)
        : new Uint8Array(input);
      let s = '', i = 0;
      while (i < b.length) {
        const c = b[i++];
        if (c < 0x80) s += String.fromCharCode(c);
        else if (c < 0xc0) s += '\\ufffd';
        else if (c < 0xe0) s += String.fromCharCode(((c & 0x1f) << 6) | (b[i++] & 0x3f));
        else if (c < 0xf0) s += String.fromCharCode(((c & 0x0f) << 12) | ((b[i++] & 0x3f) << 6) | (b[i++] & 0x3f));
        else {
          let cp = ((c & 0x07) << 18) | ((b[i++] & 0x3f) << 12) | ((b[i++] & 0x3f) << 6) | (b[i++] & 0x3f);
          cp -= 0x10000;
          s += String.fromCharCode(0xd800 + (cp >> 10), 0xdc00 + (cp & 0x3ff));
        }
      }
      return s;
    }
  };
}
if (typeof TextEncoder === 'undefined') {
  globalThis.TextEncoder = class {
    constructor() {}
    encode(s) {
      const bytes = [];
      for (let i = 0; i < s.length; i++) {
        let c = s.charCodeAt(i);
        if (c >= 0xd800 && c <= 0xdbff && i + 1 < s.length) {
          c = 0x10000 + ((c - 0xd800) << 10) + (s.charCodeAt(++i) - 0xdc00);
        }
        if (c < 0x80) bytes.push(c);
        else if (c < 0x800) bytes.push(0xc0 | (c >> 6), 0x80 | (c & 0x3f));
        else if (c < 0x10000) bytes.push(0xe0 | (c >> 12), 0x80 | ((c >> 6) & 0x3f), 0x80 | (c & 0x3f));
        else bytes.push(0xf0 | (c >> 18), 0x80 | ((c >> 12) & 0x3f), 0x80 | ((c >> 6) & 0x3f), 0x80 | (c & 0x3f));
      }
      return new Uint8Array(bytes);
    }
  };
}
`;

const out = preamble + dfJs + '\n' + processor + '\n';
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(`wrote ${outPath} (${out.length} bytes)`);
